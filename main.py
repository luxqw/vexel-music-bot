async def delete_old_player(guild_id):
    """‚úÖ –ë–µ–∑–æ–ø–∞—Å–Ω–æ–µ —É–¥–∞–ª–µ–Ω–∏–µ —Å—Ç–∞—Ä–æ–≥–æ –ø–ª–µ–µ—Ä–∞"""
    if guild_id in player_messages:
        try:
            await player_messages[guild_id].delete()
        except:
            pass
        # ‚úÖ –ò—Å–ø–æ–ª—å–∑—É–µ–º pop() –≤–º–µ—Å—Ç–æ del –¥–ª—è –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏
