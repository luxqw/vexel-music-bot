def get_queue(guild_id):
    return queues.setdefault(guild_id, [])


def create_source(video_url, title="Unknown"):
    """
    Create audio source for Discord with fresh stream URL extraction
    """
    try:
        # Create a fresh yt-dlp instance with optimal options for stream extraction
        ytdl_stream_opts = {
            "format": "bestaudio",
            "noplaylist": True,
            "quiet": True,
            "no_warnings": True,
            "extract_flat": False,
        }
        
        logger.info(f"üîç –ü–æ–ª—É—á–∞–µ–º —Å–≤–µ–∂–∏–π stream URL –¥–ª—è: {title}")
        ytdl_stream = yt_dlp.YoutubeDL(ytdl_stream_opts)
        
        # Extract fresh stream info
        stream_info = ytdl_stream.extract_info(video_url, download=False)
        
        if stream_info and stream_info.get("url"):
            stream_url = stream_info["url"]
            logger.info(f"‚úÖ –ü–æ–ª—É—á–µ–Ω —Å–≤–µ–∂–∏–π stream URL –¥–ª—è: {title}")
            
            return discord.FFmpegPCMAudio(
                stream_url,
                before_options="-reconnect 1 -reconnect_streamed 1 -reconnect_delay_max 5",
                options='-vn'
            )
        else:
            logger.error(f"‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å stream URL –¥–ª—è: {title}")
            return None
            
    except Exception as e:
        logger.error(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ stream URL –¥–ª—è '{title}': {str(e)}")
        return None


