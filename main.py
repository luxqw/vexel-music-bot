async def update_player_buttons(guild_id):
    """–ë—ã—Å—Ç—Ä–æ –æ–±–Ω–æ–≤–∏—Ç—å —Ç–æ–ª—å–∫–æ –∫–Ω–æ–ø–∫–∏ –ø–ª–µ–µ—Ä–∞ –±–µ–∑ –ø–µ—Ä–µ—Å–æ–∑–¥–∞–Ω–∏—è"""
