@tree.command(name="play", description="–í–æ—Å–ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏ –º—É–∑—ã–∫—É –∏–ª–∏ –ø–ª–µ–π–ª–∏—Å—Ç —Å YouTube")
@app_commands.describe(query="–°—Å—ã–ª–∫–∞ –∏–ª–∏ –∑–∞–ø—Ä–æ—Å")
async def play(interaction: discord.Interaction, query: str):
    log_command(interaction.user.name, "/play")

    vc = interaction.guild.voice_client
    if not vc:
        if interaction.user.voice and interaction.user.voice.channel:
            vc = await interaction.user.voice.channel.connect()
        else:
            await interaction.response.send_message("‚ö†Ô∏è –°–Ω–∞—á–∞–ª–∞ –∑–∞–π–¥–∏—Ç–µ –≤ –≥–æ–ª–æ—Å–æ–≤–æ–π –∫–∞–Ω–∞–ª.")
            return

    if query.startswith("http://") or query.startswith("https://"):
        await interaction.response.send_message(f"üîó –î–æ–±–∞–≤–ª—è—é –ø–æ —Å—Å—ã–ª–∫–µ: {query}")
    else:
        await interaction.response.send_message(f"üîç –ò—â—É: {query}")

    search_query = f"ytsearch1:{query}" if not (query.startswith("http://") or query.startswith("https://")) else query

    try:
        logger.info(f"üîç –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –∑–∞–ø—Ä–æ—Å: {search_query}")
        info = ytdl.extract_info(search_query, download=False)
        logger.info("‚úÖ –ü–æ–ª—É—á–µ–Ω–∞ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ—Ç yt-dlp")
    except Exception as e:
        await interaction.followup.send(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –∑–∞–ø—Ä–æ—Å–∞: {str(e)}")
        return

    queue = get_queue(interaction.guild.id)

    if "entries" in info:
        for entry in info["entries"]:
            queue.append({
                "title": entry["title"],
                "original_url": entry.get("original_url", entry.get("webpage_url", entry["url"])),  # Store original URL
                "requester": interaction.user.name,
            })
        await interaction.followup.send(f"üìÉ –î–æ–±–∞–≤–ª–µ–Ω –ø–ª–µ–π–ª–∏—Å—Ç: {len(info['entries'])} —Ç—Ä–µ–∫–æ–≤.")
    else:
        track = {
            "title": info["title"],
            "original_url": info.get("original_url", info.get("webpage_url", info["url"])),  # Store original URL
            "requester": interaction.user.name,
        }
        queue.append(track)
        logger.info(f"üé∂ –î–æ–±–∞–≤–ª–µ–Ω —Ç—Ä–µ–∫: {track['title']}")
        await interaction.followup.send(f"üé∂ –î–æ–±–∞–≤–ª–µ–Ω —Ç—Ä–µ–∫: {track['title']}")

    if not vc.is_playing():
        await play_next(vc, interaction.guild.id)


async def play_next(vc, guild_id):
    queue = get_queue(guild_id)
    if not queue:
        return

    next_track = queue.pop(0)
    logger.info(f"‚è≠ –°–ª–µ–¥—É—é—â–∏–π —Ç—Ä–µ–∫: {next_track['title']}")
    
    # Extract fresh stream URL for the track
    source = create_source(next_track["original_url"], next_track["title"])
    
    if source is None:
        logger.error(f"‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –∞—É–¥–∏–æ –¥–ª—è: {next_track['title']}")
        # Try the next track in queue if this one failed
        if queue:
            await play_next(vc, guild_id)
        return
    
    def after_playing(error):
        if error:
            logger.error(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–∏: {error}")
        bot.loop.create_task(play_next(vc, guild_id))
    
    try:
        vc.play(source, after=after_playing)
        logger.info(f"üéµ –ò–≥—Ä–∞–µ—Ç: {next_track['title']}")
    except Exception as e:
        logger.error(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–ø—É—Å–∫–µ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è: {str(e)}")
        # Try the next track if this one failed
        if queue:
            await play_next(vc, guild_id)


