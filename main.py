@tree.command(name="play", description="–í–æ—Å–ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏ –º—É–∑—ã–∫—É –∏–ª–∏ –ø–ª–µ–π–ª–∏—Å—Ç —Å YouTube")
@app_commands.describe(query="–°—Å—ã–ª–∫–∞ –∏–ª–∏ –∑–∞–ø—Ä–æ—Å")
async def play(interaction: discord.Interaction, query: str):
    log_command(interaction.user.name, "/play")

    vc = interaction.guild.voice_client
    if not vc:
        if interaction.user.voice and interaction.user.voice.channel:
            vc = await interaction.user.voice.channel.connect()
        else:
            await interaction.response.send_message("‚ö†Ô∏è –°–Ω–∞—á–∞–ª–∞ –∑–∞–π–¥–∏—Ç–µ –≤ –≥–æ–ª–æ—Å–æ–≤–æ–π –∫–∞–Ω–∞–ª.")
            return

    if query.startswith("http://") or query.startswith("https://"):
        await interaction.response.send_message(f"üîó –î–æ–±–∞–≤–ª—è—é –ø–æ —Å—Å—ã–ª–∫–µ: {query}")
    else:
        await interaction.response.send_message(f"üîç –ò—â—É: {query}")

    search_query = f"ytsearch1:{query}" if not (query.startswith("http://") or query.startswith("https://")) else query

    try:
        info = ytdl.extract_info(search_query, download=False)
    except Exception as e:
        await interaction.followup.send_message(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –∑–∞–ø—Ä–æ—Å–∞: {str(e)}")
        return

    queue = get_queue(interaction.guild.id)

    if "entries" in info:
        for entry in info["entries"]:
            queue.append({
                "title": entry["title"],
                "url": entry["url"],  # –í–µ—Ä–Ω—É–ª–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ url
                "requester": interaction.user.name,
            })
        await interaction.followup.send(f"üìÉ –î–æ–±–∞–≤–ª–µ–Ω –ø–ª–µ–π–ª–∏—Å—Ç: {len(info['entries'])} —Ç—Ä–µ–∫–æ–≤ (–∑–∞–∫–∞–∑–∞–ª: {interaction.user.name}).")
    else:
        track = {
            "title": info["title"],
            "url": info["url"],  # –í–µ—Ä–Ω—É–ª–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ url
            "requester": interaction.user.name,
        }
        queue.append(track)
        await interaction.followup.send(f"üé∂ –î–æ–±–∞–≤–ª–µ–Ω —Ç—Ä–µ–∫: {track['title']} (–∑–∞–∫–∞–∑–∞–ª: {interaction.user.name})")

    if not vc.is_playing():
        await play_next(vc, interaction.guild.id)


async def play_next(vc, guild_id):
    queue = get_queue(guild_id)
    if not queue:
        current_tracks[guild_id] = None  # Clear current track when queue is empty
        return

    next_track = queue.pop(0)
    current_tracks[guild_id] = next_track  # Store current track info
    source = create_source(next_track["url"])

    # Send notification about now playing track
    guild = bot.get_guild(guild_id)
    if guild:
        # Find a text channel to send the now playing message
        text_channel = None
        for channel in guild.text_channels:
            if channel.permissions_for(guild.me).send_messages:
                text_channel = channel
                break
        
        if text_channel:
            await text_channel.send(f"üéµ –°–µ–π—á–∞—Å –∏–≥—Ä–∞–µ—Ç: {next_track['title']} (–∑–∞–∫–∞–∑–∞–ª: {next_track['requester']})")

    vc.play(source, after=lambda e: bot.loop.create_task(play_next(vc, guild_id)))


